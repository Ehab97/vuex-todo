<template>
  <div>
       <h3>Add Todo</h3>
       <div class="add">
            <form @submit="onSubmit">
                 <input type="text" v-model="title" placeholder="add Todo">
                 <input type="submit" value="submit">
            </form>
       </div>
  </div>
</template>

<script>
import {mapActions} from 'vuex'
export default {
 name:'Add Todo',
 data(){
      return{
           title:''
      }
 },methods:{
      ...mapActions(['addTodo']),
      onSubmit(e){
           e.preventDefault();
           this.addTodo(this.title);
      }
 }    
}
</script>

